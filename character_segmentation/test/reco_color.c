    int blue_idx = 2;
    int b = (src)[blue_idx], g = (src)[1], r = (src)[2^blue_idx];
    int h, s, v = b;
    int vmin = b, diff;
    int vr, vg;
 
    CALC_MAX_8U( v, g );
    CALC_MAX_8U( v, r );
    CALC_MIN_8U( vmin, g );
    CALC_MIN_8U( vmin, r );
 
    diff = v - vmin;
    vr = v == r ? -1 : 0;
    vg = v == g ? -1 : 0;
 
    s = diff * div_table[v] >> hsv_shift;
    h = (vr & (g - b)) +
        (~vr & ((vg & (b - r + 2 * diff)) + ((~vg) & (r - g + 4 * diff))));
